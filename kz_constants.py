"""KZ 2026 regulatory constants for ESF invoice validation.

Sources:
- Закон РК № 239-VIII от 08.12.2025 (О республиканском бюджете на 2026-2028)
- Налоговый кодекс РК № 214-VIII от 18.07.2025 (действует с 01.01.2026)
- Приказ МФ РК № 629 от 28.10.2025 (ЭСФ)
- Приказ МТИ РК № 284-НҚ от 02.10.2025 (Национальный каталог товаров)
"""

from __future__ import annotations

# ---------------------------------------------------------------------------
# Budget Law 2026 — base calculation indicators
# ---------------------------------------------------------------------------
MRP = 4_325  # Месячный расчетный показатель (МРП)
MZP = 85_000  # Минимальная заработная плата (МЗП)
SUBSISTENCE_MIN = 50_851  # Прожиточный минимум (ПМ)

# ---------------------------------------------------------------------------
# VAT (НДС) — rates effective Jan 1, 2026
# ---------------------------------------------------------------------------
VAT_RATE_BASE = 0.16  # 16% (was 12% until 2025)
VAT_RATE_MEDICINES = 0.05  # 5% — лекарственные средства
VAT_RATE_PUBLICATIONS = 0.10  # 10% — печатные издания
VAT_RATES_VALID = {0.16, 0.10, 0.05, 0.0}  # all legal rates incl. exempt
VAT_RATE_TOLERANCE = 0.005  # tolerance for matching (0.5%)

# VAT registration threshold (порог постановки на учёт по НДС)
VAT_REGISTRATION_THRESHOLD = 10_000 * MRP  # 43,250,000 KZT

# ---------------------------------------------------------------------------
# Currency whitelist for ESF invoices
# ---------------------------------------------------------------------------
VALID_CURRENCIES = {"KZT", "USD", "EUR", "RUB", "GBP", "CNY"}

# ---------------------------------------------------------------------------
# ESF electronic invoice rules
# ---------------------------------------------------------------------------
ESF_CORRECTION_CONFIRM_DAYS = 10  # двустороннее подтверждение (дней)

# ---------------------------------------------------------------------------
# Amount sanity bounds
# ---------------------------------------------------------------------------
AMOUNT_SUSPICIOUS_HIGH = VAT_REGISTRATION_THRESHOLD  # single-invoice flag
AMOUNT_MIN_PLAUSIBLE = 1.0  # minimum plausible amount in KZT

# ---------------------------------------------------------------------------
# Debt threshold — auto bank-account block
# ---------------------------------------------------------------------------
DEBT_BLOCK_THRESHOLD = 20 * MRP  # 86,500 KZT
